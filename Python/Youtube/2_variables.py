#variable = a container for a value. Behaves as the value thats it contains
#------------ str -------------------------
first_name = "Diep"
last_name = "Au"
full_name = first_name + " " + last_name
print(type(full_name))
print("Hello " + full_name)

#------------- int -----------------------------
age = 21
age = age + 1
print(type(age))
print("Your age is: " + str(age))

#------------- float ------------------------------
height = 165.5
print(type(height))
print("Your height is: " + str(height) + "cm")

#------------- bool -----------------------------
human = True
print(type(human))
print("Are you a human: " + str(human))
 